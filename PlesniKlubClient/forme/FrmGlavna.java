    private void setujMod(ModGlavne mod) {
        this.mod = mod;
        switch (mod) {
            case PRAZNA:
                pnlPretraga.setVisible(false);
                break;
            case PRETRAGA_PLESACA:
                tblRezultat.setModel(new ModelTabelePlesac(new ArrayList<>()));
                lblKriterijum1.setText("ime i prezime: ");
                lblKriterijum2.setText("broj godina: ");
                pnlPretraga.setVisible(true);
                break;
            case PRETRAGA_TAKMICENJA:
                tblRezultat.setModel(new ModelTabeleTakmicenje(new ArrayList<>()));
                lblKriterijum1.setText("mesto: ");
                lblKriterijum2.setText("termin: ");
                pnlPretraga.setVisible(true);
                break;
        }
    }

    public void pretraga() {
        if (mod == ModGlavne.PRETRAGA_PLESACA) {
            pretragaPlesaca();
        }
        if (mod == ModGlavne.PRETRAGA_TAKMICENJA) {
            pretragaTakmicenja();
        }
    }

    private void pretragaPlesaca() {
        try{
            Plesac plesac = new Plesac();
            plesac.setIme(txtKriterijum1.getText());
            plesac.setPrezime(txtKriterijum1.getText());
            plesac.setBrojGodina(-1);
            try{
                plesac.setBrojGodina(Integer.parseInt(txtKriterijum2.getText()));
            }catch(Exception ex){
            }
            List<Plesac> lista = Kontroler.getInstance().pretragaPlesaca(plesac);
            ((ModelTabelePlesac)tblRezultat.getModel()).setLista(lista);
        }catch(Exception ex){            
            //Logger.getLogger(FrmGlavna.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }

    private void pretragaTakmicenja() {
        try {
            Takmicenje takmicenje = new Takmicenje();
            takmicenje.setMesto(txtKriterijum1.getText());
            try {
                takmicenje.setTermin(Kontroler.getInstance().parseDatum(txtKriterijum2.getText()));
            } catch (Exception ex) {
            }
            List<Takmicenje> lista = Kontroler.getInstance().pretragaTakmicenja(takmicenje);
            ((ModelTabeleTakmicenje)tblRezultat.getModel()).setLista(lista);
        } catch (Exception ex) {            
            //Logger.getLogger(FrmGlavna.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }
