    private void btnDetaljiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetaljiActionPerformed
        int i = tblRezultat.getSelectedRow();
        if (i == -1) {
            JOptionPane.showMessageDialog(this, "Izaberite red iz tabele");;
            return;
        }
        if (mod == ModGlavne.PRETRAGA_PLESACA) {
            Plesac plesac = ((ModelTabelePlesac) tblRezultat.getModel()).getRed(i);
            new FrmPlesac(this, true, plesac, ModDomenske.PRIKAZ).setVisible(true);
        }
        if (mod == ModGlavne.PRETRAGA_TAKMICENJA) {
            Takmicenje takmicenje = ((ModelTabeleTakmicenje) tblRezultat.getModel()).getRed(i);
            new FrmTakmicenje(this, true, takmicenje, ModDomenske.PRIKAZ).setVisible(true);
        }
    }//GEN-LAST:event_btnDetaljiActionPerformed


